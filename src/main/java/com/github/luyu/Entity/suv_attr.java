package com.github.luyu.Entity;

public class suv_attr {
    private int suv_id;
    private String cha_height;
    private String tyre;
    private String direction_sys;
    private String damping;
    private int type;

    public int getSuv_id() {
        return suv_id;
    }

    public void setSuv_id(int suv_id) {
        this.suv_id = suv_id;
    }

    public String getCha_height() {
        return cha_height;
    }

    public void setCha_height(String cha_height) {
        this.cha_height = cha_height;
    }

    public String getTyre() {
        return tyre;
    }

    public void setTyre(String tyre) {
        this.tyre = tyre;
    }

    public String getDirection_sys() {
        return direction_sys;
    }

    public void setDirection_sys(String direction_sys) {
        this.direction_sys = direction_sys;
    }

    public String getDamping() {
        return damping;
    }

    public void setDamping(String damping) {
        this.damping = damping;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
